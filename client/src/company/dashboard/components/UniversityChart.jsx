import {
  ResponsiveContainer,
  BarChart,
  Bar,
  XAxis,
  YAxis,
  Tooltip,
  CartesianGrid,
} from "recharts";

const UniversityChart = ({ data }) => {
    if (!data || data.length === 0) {
    return (
      <div className="bg-white rounded-[2rem] p-6 shadow-sm border border-gray-100 flex items-center justify-center h-[450px]">
        <p className="text-gray-400 font-bold text-sm">No university data yet</p>
      </div>
    );
  }
  return (
    <div className="bg-white rounded-[2rem] p-6 shadow-sm border border-gray-100">
      <div className="mb-6">
        <h2 className="text-2xl font-black text-[#1A1A1A]">
          University Revenue
        </h2>

        <p className="text-sm text-gray-500 mt-1">
          Revenue generated by universities
        </p>
      </div>

      <div className="h-[350px]">
        <ResponsiveContainer width="100%" height="100%">
          <BarChart data={data}>
            <CartesianGrid
              strokeDasharray="3 3"
              vertical={false}
            />

            <XAxis
              dataKey="universityName"
              tick={{ fontSize: 12 }}
            />

            <YAxis tick={{ fontSize: 12 }} />

            <Tooltip />

            <Bar
              dataKey="revenue"
              radius={[10, 10, 0, 0]}
            />
          </BarChart>
        </ResponsiveContainer>
      </div>
    </div>
  );
};

export default UniversityChart;